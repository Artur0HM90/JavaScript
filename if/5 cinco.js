/* 5 - Escribe un programa que determine si un número es divisible por 5. */

let ingresaNumero = prompt ("Ingresa un número.");

if (ingresaNumero % 5 == 0) {
    console.log("El número " + ingresaNumero + " si es divisible por 5.");

}else{
    console.log("El número " + ingresaNumero + " no es divisible por 5.")
}